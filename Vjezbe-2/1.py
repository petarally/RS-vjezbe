# Kvadriranje broja

kvadrat_broja = lambda x: x ** 2

# Zbroji pa kvadriraj dva broja

zbroji_i_kvadriraj = lambda a, b: (a + b) ** 2

# Kvadriranje duljine niza

kvadriraj_duljinu = lambda niz: len(niz) ** 2

# množenje vrijednosti s 5, pa potenciranje na x

mnozi_i_potenciraj = lambda x, y: (y * 5) ** x

# True ako je broj paran, inače None

paran = lambda x: x if x % 2 == 0 else None
